function generateDates(startDate, endDate) {
  const start = new Date(startDate);
  const end = new Date(endDate);
  const dates = [];

  while (start <= end) {
    dates.push(start.toISOString().split('T')[0]);  // Μορφή "YYYY-MM-DD"
    start.setDate(start.getDate() + 1);  // Προσθήκη μιας ημέρας
  }

  return dates;
}

